import { readFile, writeFile, mkdir } from 'node:fs/promises';
import crypto from 'node:crypto';
import path from 'node:path';
const root = process.cwd();
const artifactPath = process.argv[2] || path.join(root,'data/generated/cms-dialysis.generated.json');
const cachePath = process.argv[3] || path.join(root,'data/generated/geocoding-cache/geocoding-cache-v0.3.0.json');
const outPath = process.argv[4] || path.join(root,'data/reports/address-geocoding-delta-v0.3.0.json');
const normalize = (parts)=>parts.filter(Boolean).join(' ').toUpperCase().replace(/[^A-Z0-9]+/g,' ').trim();
const sha = (s)=>crypto.createHash('sha256').update(s).digest('hex');
const artifact = JSON.parse(await readFile(artifactPath,'utf8'));
const cache = JSON.parse(await readFile(cachePath,'utf8'));
const cacheByFacility = new Map((cache.entries||[]).map(e=>[`${e.sourceId}:${e.sourceFacilityId}`,e]));
const records = artifact.records || [];
const classifications = records.map((r,i)=>{
  const addressLine = r.addressLine || r.address || r.streetAddress || r['Address'] || r['Street Address'] || '';
  const city = r.city || r['City'] || '';
  const state = r.state || r['State'] || '';
  const zip = r.zip || r.zipCode || r['ZIP Code'] || r['Zip Code'] || '';
  const sourceFacilityId = String(r.id || r.cmsCertificationNumber || r.providerId || r['CMS Certification Number (CCN)'] || r['Facility ID'] || i);
  const normalizedAddress = normalize([addressLine,city,state,zip]);
  const addressHash = normalizedAddress ? sha(normalizedAddress) : null;
  const key = `${artifact.metadata.sourceId}:${sourceFacilityId}`;
  const cached = cacheByFacility.get(key);
  let classification = 'new-address-needs-geocoding';
  if (!normalizedAddress) classification = 'missing-address-not-geocodable';
  else if (cached?.addressHash === addressHash && cached.status === 'matched') classification = 'unchanged-geocode-cache-hit';
  else if (cached && cached.addressHash !== addressHash) classification = 'changed-address-needs-geocoding';
  else if (r.latitude && r.longitude && r.geocodingStatus === 'matched') classification = 'already-geocoded';
  return {sourceId: artifact.metadata.sourceId, sourceFacilityId, normalizedAddress, addressHash, classification};
});
const summary = classifications.reduce((a,r)=>{a[r.classification]=(a[r.classification]||0)+1; return a;},{});
const report={schemaVersion:'address-geocoding-delta-v0.3.0',generatedAt:'2026-07-10T00:00:00.000Z',artifactPath:path.relative(root,artifactPath),cachePath:path.relative(root,cachePath),sourceId:artifact.metadata.sourceId,recordCount:records.length,summary,classifications,networkCalls:false,liveGeocoding:false};
await mkdir(path.dirname(outPath),{recursive:true}); await writeFile(outPath,JSON.stringify(report,null,2)+'\n');
console.log(`Wrote ${path.relative(root,outPath)}`);
